 /**
 * This script integrates a migration plan generated by the migration solver into a graph.
 * @author Hokeun Kim
 */

"use strict";

var fs = require('fs');
var JSON2 = require('JSON2');

var program = require('commander');
program
  .version('0.1.0')
  .option('-g, --graph [value]', 'Input graph file')
  .option('-m, --migration-plan [value]', 'Migration plan file')
  .option('-o, --output [value]', 'Output graph file with migration plan integrated')
  .parse(process.argv);

var graphFile = program.graph;
var migrationPlanFile = program.migrationPlan;
var outputFile = program.output;

if (graphFile == null) {
	console.log('Graph file path is missing!');
	program.outputHelp();
	process.exit(1);
}
if (migrationPlanFile == null) {
	console.log('Migration plan file path is missing!');
	program.outputHelp();
	process.exit(1);
}
if (outputFile == null) {
	console.log('Output file path is missing!');
	program.outputHelp();
	process.exit(1);
}

console.log('Graph file path: ' + graphFile);
console.log('Migration plan file path: ' + migrationPlanFile);
console.log('Output file path: ' + outputFile);

var graph = JSON.parse(fs.readFileSync(graphFile));
var migrationPlan = JSON.parse(fs.readFileSync(migrationPlanFile));

var backupMap = {};

for (var i = 0; i < migrationPlan.echoServerList.length; i++) {
	var server = migrationPlan.echoServerList[i];
	backupMap[server['name']] = server['backupTo'];
}

for (var i = 0; i < migrationPlan.autoClientList.length; i++) {
	var client = migrationPlan.autoClientList[i];
	backupMap[client['name']] = client['backupTo'];
}

//console.log(backupMap);

for (var i = 0; i < graph.entityList.length; i++) {
	var entityName = graph.entityList[i]['name'];
	graph.entityList[i]['backupToAuthIds'] = backupMap[entityName];
}

console.log('Writing integrated graph to ' + outputFile + ' ...');
fs.writeFileSync(outputFile,
    JSON2.stringify(graph, null, '\t'),
    'utf8'
);